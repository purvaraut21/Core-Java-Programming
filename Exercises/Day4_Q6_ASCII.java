package Exercise;

public class Day4_Q6_ASCII {

	public static void main(String[] args) {
		// WAP to print ASCII value of A, Z, a, z, @, &.
		
		// Printing the ASCII values of each character
        System.out.println("The ASCII value of 'A' is: " + (int)'A');
        System.out.println("The ASCII value of 'Z' is: " + (int)'Z');
        System.out.println("The ASCII value of 'a' is: " + (int)'a');
        System.out.println("The ASCII value of 'z' is: " + (int)'z');
        System.out.println("The ASCII value of '@' is: " + (int)'@');
        System.out.println("The ASCII value of '&' is: " + (int)'&');

	}

}
